    private void generateId() {
        txtSupplierID.setEditable(false);
        ArrayList<Supplier> list = SupplierController.getInstance().getList();
        int rowCount = list.size();
        if (rowCount == 0) {
            txtSupplierID.setText("NCC0" + (rowCount + 1) + "");
            return;
        } else {
            String lastId = list.get(list.size() - 1).getsMaNCC().trim();
            int numberId = Integer.parseInt(lastId.replace("NCC", ""));
            txtSupplierID.setText("NCC0" + (numberId + 1) + "");
        }

//        int row = SupplierController.getInstance().CountingRow();
//        this.setTxtSupplierID("NCC0" + (row + 1) + "");
    }

