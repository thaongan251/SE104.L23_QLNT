    private void btnAddSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSupplierActionPerformed
        try {
            String sMaNCC = txtSupplierID.getText();
            String sTenNCC = txtSupplierName.getText();
            String SDT = txtSupplierPhone.getText();
            String sDiaChi = txtSupplierAddress.getText();
            String sEmail = txtSupplierEmail.getText();
            // Đổi từ java.util.Date (JCalendar xài) -> java.time.LocalDate (model xài) 
            LocalDate dNgayDangKyHD = txtContractSignDate.getDate().toInstant()
                    .atZone(ZoneId.systemDefault()).toLocalDate();
            int tThoiHanHD = Integer.parseInt(txtContractExpiredDate.getValue().toString());

            Supplier supplier = new Supplier(sMaNCC, sTenNCC,
                    SDT, sDiaChi, sEmail, dNgayDangKyHD, tThoiHanHD);

            if (isEditing) {
                SupplierController.getInstance().getList().set(this.index, supplier);
                this.setVisible(true);
                SupplierController.getInstance().deleteFromDatabase(
                        SupplierController.getInstance().getList().get(this.index)
                );
                SupplierController.getInstance().insertToDatabase(supplier);

//                SupplierController.getInstance().Delete(SupplierController.getInstance()
//                        .getList().get(index).getsMaNCC());
            } else {
                SupplierController.getInstance().getList().add(supplier);
                SupplierController.getInstance().insertToDatabase(supplier);
//                SupplierController.getInstance().Insert(sMaNCC, sTenNCC, SDT, sDiaChi,
//                        sEmail, dNgayDangKyHD.toString(), tThoiHanHD);
            }

            PanelSupplier.getInstance().getTable().setModel(
                    SupplierController.getInstance().toTable()
            );

            if (isEditing) {
                this.dispose();
                return;
            }

            // clear info after adding successfully
            this.btnClearSupplierActionPerformed(evt);
            this.setPlaceHolder();
            //generate new ID
            this.generateId();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Nhập sai thông tin", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddSupplierActionPerformed


